package dev.psygamer.wireframe.util.math.vector;

public class Vector3f {
	
	private final float x, y, z;
	
	public Vector3f(final float x, final float y, final float z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public float getX() {
		return this.x;
	}
	
	public float getY() {
		return this.y;
	}
	
	public double getZ() {
		return this.z;
	}
}
